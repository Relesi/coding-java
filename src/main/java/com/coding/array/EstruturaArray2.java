package com.coding.array;

public class EstruturaArray2 {
    public static void main(String[] args) {
        String[] nomes = new String[5];

        nomes[0] = "João";
        nomes[1] = "Maria";
        nomes[2] = "Bob";
        nomes[3] = "José";
        nomes[4] = "Seven";

        for (int i = 0; i < nomes.length; i++){
            System.out.println(nomes[i]);
        }




    }

}
