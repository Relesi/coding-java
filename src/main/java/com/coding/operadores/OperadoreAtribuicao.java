package com.coding.operadores;

public class OperadoreAtribuicao {

    public static void main(String[] args) {
        // = += -= /= %=


        double bonus = 1800;
        bonus += 1000;
        bonus -= 1000;
        bonus *= 2;
        bonus /= 2;
        bonus %= 2;

        bonus = 2000;


        System.out.println(bonus);
    }
}
